def resolve_partition_info(tid: int, tid_v2: int, tname: str = "", tname_v2: str = "") -> tuple[str, int, str, int]:
    direct_name = (tname_v2 or tname or "").strip()
    if direct_name:
        return direct_name, int(tid_v2 or tid or 0), _guess_parent_name(direct_name, tid, tid_v2), _guess_parent_id(tid, tid_v2)

    if tid_v2 in TID_V2_NAMES:
        name, parent_name = TID_V2_NAMES[tid_v2]
        return name, tid_v2, parent_name, tid_v2

    if tid in SUB_PARTITION_NAMES:
        name, parent_name = SUB_PARTITION_NAMES[tid]
        return name, tid, parent_name, tid

    if tid in PARENT_PARTITION_NAMES:
        parent_name = PARENT_PARTITION_NAMES[tid]
        return parent_name, tid, parent_name, tid

    if tid_v2 or tid:
        return "未知分类", int(tid_v2 or tid or 0), "未知分类", int(tid_v2 or tid or 0)
    return "未知分类", 0, "未知分类", 0


def _guess_parent_name(name: str, tid: int, tid_v2: int) -> str:
    if tid in SUB_PARTITION_NAMES:
        return SUB_PARTITION_NAMES[tid][1]
    if tid in PARENT_PARTITION_NAMES:
        return PARENT_PARTITION_NAMES[tid]
    if tid_v2 in TID_V2_NAMES:
        return TID_V2_NAMES[tid_v2][1]
    return name


def _guess_parent_id(tid: int, tid_v2: int) -> int:
    return int(tid_v2 or tid or 0)
